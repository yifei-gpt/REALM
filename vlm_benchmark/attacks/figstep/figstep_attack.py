"""FigStep typographic injection attack.

Presents a clean image alongside a rendered text annotation image to
make a VLM hallucinate the described object.  Injection text is always
generated by an LLM server (vLLM / OpenAI-compatible).

If no server URL is provided, the attack auto-starts a vLLM server on a
free port, uses it for the lifetime of the attack, and shuts it down on
garbage collection or interpreter exit.
"""

import atexit
import dataclasses
import logging
import os
import re
import socket
import subprocess
import sys
import tempfile
import textwrap
import time
import urllib.request

from PIL import Image, ImageDraw, ImageFont

logger = logging.getLogger(__name__)

from ..base_attack import AttackResult, BaseAttack
from ...data.base_dataset import Sample
from .config import FigStepConfig, _INJECTION_PROMPT

_DEFAULT_LLM_MODEL = "Qwen/Qwen3-8B"
_VLLM_STARTUP_TIMEOUT = 300  # seconds


class FigStepAttack(BaseAttack):

    FONT_PATH = "/usr/share/fonts/liberation-mono/LiberationMono-Bold.ttf"

    def __init__(self, config: FigStepConfig):
        super().__init__(config)
        self._llm_client = None
        self._vllm_proc = None

    def is_gradient_based(self) -> bool:
        return False  # managed subprocess, if we auto-started it

    def __del__(self):
        self._stop_server()

    # -- vLLM server auto-start ------------------------------------------------

    @staticmethod
    def _find_free_port() -> int:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.bind(("", 0))
            return s.getsockname()[1]

    @staticmethod
    def _wait_for_server(url: str, timeout: int = _VLLM_STARTUP_TIMEOUT) -> bool:
        deadline = time.time() + timeout
        endpoint = f"{url}/v1/models"
        while time.time() < deadline:
            try:
                urllib.request.urlopen(endpoint, timeout=5)
                return True
            except Exception:
                time.sleep(3)
        return False

    def _start_server(self) -> str:
        """Launch a vLLM server on a free port. Returns the server URL."""
        model = self.config.llm_model or _DEFAULT_LLM_MODEL
        port = self._find_free_port()

        # Pick GPU from device string: "cuda:1" -> "1", "cuda" -> "0"
        device = self.config.device or "cuda"
        if ":" in device:
            gpu_id = device.split(":")[1]
        elif device.startswith("cuda"):
            gpu_id = "0"
        else:
            gpu_id = "0"

        env = os.environ.copy()
        env["CUDA_VISIBLE_DEVICES"] = gpu_id

        cmd = [
            sys.executable, "-m", "vllm.entrypoints.openai.api_server",
            "--model", model,
            "--port", str(port),
            "--max-model-len", "2048",
            "--gpu-memory-utilization", "0.6",
        ]
        log_file = tempfile.NamedTemporaryFile(
            prefix="figstep_vllm_", suffix=".log", delete=False, mode="w",
        )
        logger.info("Starting vLLM server: %s on GPU %s, port %d",
                     model, gpu_id, port)
        logger.info("  Log: %s", log_file.name)

        self._vllm_proc = subprocess.Popen(
            cmd, env=env, stdout=log_file, stderr=subprocess.STDOUT,
        )

        # Ensure cleanup on interpreter exit
        proc = self._vllm_proc
        def _kill():
            if proc.poll() is None:
                proc.terminate()
                try:
                    proc.wait(timeout=10)
                except subprocess.TimeoutExpired:
                    proc.kill()
                logger.info("Stopped FigStep vLLM server (pid %d)", proc.pid)
        atexit.register(_kill)

        url = f"http://localhost:{port}"
        logger.info("  Waiting for server (up to %ds)...", _VLLM_STARTUP_TIMEOUT)
        if not self._wait_for_server(url):
            self._vllm_proc.terminate()
            raise RuntimeError(
                f"FigStep vLLM server failed to start. Check log: {log_file.name}"
            )
        logger.info("  Server ready at %s", url)
        return url

    def _stop_server(self):
        if self._vllm_proc is not None and self._vllm_proc.poll() is None:
            self._vllm_proc.terminate()
            try:
                self._vllm_proc.wait(timeout=10)
            except subprocess.TimeoutExpired:
                self._vllm_proc.kill()
            logger.info("Stopped FigStep vLLM server (pid %d)",
                        self._vllm_proc.pid)
            self._vllm_proc = None

    # -- LLM injection text generation -----------------------------------------

    def _get_llm_client(self):
        """Lazy-init an OpenAI-compatible client, auto-starting vLLM if needed."""
        if self._llm_client is not None:
            return self._llm_client

        url = self.config.llm_server_url
        if not url:
            # Auto-start a vLLM server
            url = self._start_server()
            self.config.llm_server_url = url

        if not self.config.llm_model:
            self.config.llm_model = _DEFAULT_LLM_MODEL

        from openai import OpenAI
        self._llm_client = OpenAI(base_url=f"{url}/v1", api_key="dummy")
        return self._llm_client

    def _llm_generate_injection(self, target_text: str) -> str:
        """Ask the LLM server to generate injection text for a target."""
        client = self._get_llm_client()
        prompt = _INJECTION_PROMPT.replace("{target}", target_text)
        resp = client.chat.completions.create(
            model=self.config.llm_model,
            messages=[
                {"role": "system",
                 "content": "You are a concise assistant. /no_think"},
                {"role": "user", "content": prompt},
            ],
            max_tokens=256, temperature=0.0,
        )
        text = resp.choices[0].message.content or ""
        # Strip thinking tags if present
        if "<think>" in text:
            text = re.sub(r"<think>.*?</think>\s*", "", text,
                          flags=re.DOTALL)
        if "<think>" in text:
            text = re.sub(r"<think>.*", "", text, flags=re.DOTALL)
        text = text.strip().strip("\"'.")
        if not text:
            raise RuntimeError(
                f"LLM returned empty injection text for target '{target_text}'"
            )
        return text

    # -- Injection image rendering ---------------------------------------------

    def _make_injection_image(self, text: str) -> Image.Image:
        w, h = self.config.injection_image_size
        margin = 20
        usable_w, usable_h = w - 2 * margin, h - 2 * margin

        def _load_font(size):
            try:
                return ImageFont.truetype(self.FONT_PATH, size)
            except Exception:
                logger.warning("Font %s not found, using default",
                               self.FONT_PATH)
                return ImageFont.load_default()

        def _layout(font):
            """Wrap text and return (lines, line_height, total_height)."""
            char_w = font.getbbox("A")[2]
            max_chars = max(1, usable_w // char_w)
            lines = textwrap.wrap(text, width=max_chars) or [text]
            line_h = font.getbbox("Ag")[3] - font.getbbox("Ag")[1]
            spacing = int(line_h * 0.3)
            total_h = len(lines) * (line_h + spacing) - spacing
            return lines, line_h, spacing, total_h

        # Binary search for largest font size that fits
        lo, hi = 16, self.config.font_size
        best_size = lo
        for _ in range(10):
            mid = (lo + hi + 1) // 2
            font = _load_font(mid)
            _, _, _, total_h = _layout(font)
            if total_h <= usable_h:
                best_size = mid
                lo = mid
            else:
                hi = mid - 1

        font = _load_font(best_size)
        lines, line_h, spacing, _ = _layout(font)

        img = Image.new("RGB", (w, h), color="white")
        draw = ImageDraw.Draw(img)
        _, _, _, total_h = _layout(font)
        y = margin + (usable_h - total_h) // 2
        for line in lines:
            draw.text((margin, y), line, fill="black", font=font)
            y += line_h + spacing
        return img

    # -- Target resolution -----------------------------------------------------

    def _resolve_target(self, sample: Sample) -> str:
        """Get target_text from config or sample metadata."""
        if self.config.target_text:
            return self.config.target_text
        target = sample.metadata.get("attack_target_text")
        if target:
            return target
        raise ValueError(
            f"No target_text: set --target_text or provide "
            f"metadata['attack_target_text'] on sample '{sample.id}'."
        )

    # -- Main entry point ------------------------------------------------------

    def generate(self, model, sample: Sample, **kwargs) -> AttackResult:
        target_text = self._resolve_target(sample)

        # Use pre-computed injection from metadata if the caller already ran
        # the LLM (e.g. generate.py batched it), otherwise generate here.
        injection_text = sample.metadata.get("attack_injection_text")
        if not injection_text:
            injection_text = self._llm_generate_injection(target_text)

        clean_image = sample.images[0]
        injection_image = self._make_injection_image(injection_text)

        original_output = ""
        adversarial_output = ""
        success = False

        if model is not None:
            original_output = self._run_inference_multi(
                model, sample, sample.question
            )
            adv_sample = dataclasses.replace(
                sample,
                images=[clean_image, injection_image],
                question=self.config.text_prompt,
            )
            adversarial_output = self._run_inference_multi(
                model, adv_sample, self.config.text_prompt
            )
            success = target_text.lower() in adversarial_output.lower()

        return AttackResult(
            success=success,
            adversarial_sample=injection_image,
            original_output=original_output,
            adversarial_output=adversarial_output,
            perturbation_norm=0.0,
            queries=2 if model is not None else 0,
            metadata={
                "target_phrase": target_text,
                "injection_text": injection_text,
                "text_prompt": self.config.text_prompt,
                "attack": "figstep",
            },
        )
