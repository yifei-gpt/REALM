"""PromptInject: Text-level adversarial suffix injection for VLM hallucination.

Injection text is always generated by an LLM server (vLLM / OpenAI-compatible).
If no server URL is provided, the attack auto-starts a vLLM server on a free
port, uses it for the lifetime of the attack, and shuts it down on exit.
"""

import atexit
import dataclasses
import logging
import os
import re
import socket
import subprocess
import sys
import tempfile
import time
import urllib.request

logger = logging.getLogger(__name__)

from ..base_attack import AttackResult, BaseAttack
from ...data.base_dataset import Sample
from .config import (
    PromptInjectConfig,
    _INJECTION_PROMPT_INSTRUCTION,
    _INJECTION_PROMPT_AUTHORITY,
)

_DEFAULT_LLM_MODEL = "Qwen/Qwen3-8B"
_VLLM_STARTUP_TIMEOUT = 300  # seconds


class PromptInjectAttack(BaseAttack):

    def __init__(self, config: PromptInjectConfig):
        super().__init__(config)
        self._llm_client = None
        self._vllm_proc = None

    def __del__(self):
        self._stop_server()

    def is_gradient_based(self) -> bool:
        return False

    # -- vLLM server auto-start ------------------------------------------------

    @staticmethod
    def _find_free_port() -> int:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.bind(("", 0))
            return s.getsockname()[1]

    @staticmethod
    def _wait_for_server(url: str, timeout: int = _VLLM_STARTUP_TIMEOUT) -> bool:
        deadline = time.time() + timeout
        endpoint = f"{url}/v1/models"
        while time.time() < deadline:
            try:
                urllib.request.urlopen(endpoint, timeout=5)
                return True
            except Exception:
                time.sleep(3)
        return False

    def _start_server(self) -> str:
        """Launch a vLLM server on a free port. Returns the server URL."""
        model = self.config.llm_model or _DEFAULT_LLM_MODEL
        port = self._find_free_port()

        device = self.config.device or "cuda"
        if ":" in device:
            gpu_id = device.split(":")[1]
        elif device.startswith("cuda"):
            gpu_id = "0"
        else:
            gpu_id = "0"

        env = os.environ.copy()
        env["CUDA_VISIBLE_DEVICES"] = gpu_id

        cmd = [
            sys.executable, "-m", "vllm.entrypoints.openai.api_server",
            "--model", model,
            "--port", str(port),
            "--max-model-len", "2048",
            "--gpu-memory-utilization", "0.6",
        ]
        log_file = tempfile.NamedTemporaryFile(
            prefix="promptinject_vllm_", suffix=".log", delete=False, mode="w",
        )
        logger.info("Starting vLLM server: %s on GPU %s, port %d",
                     model, gpu_id, port)
        logger.info("  Log: %s", log_file.name)

        self._vllm_proc = subprocess.Popen(
            cmd, env=env, stdout=log_file, stderr=subprocess.STDOUT,
        )

        proc = self._vllm_proc
        def _kill():
            if proc.poll() is None:
                proc.terminate()
                try:
                    proc.wait(timeout=10)
                except subprocess.TimeoutExpired:
                    proc.kill()
                logger.info("Stopped PromptInject vLLM server (pid %d)",
                            proc.pid)
        atexit.register(_kill)

        url = f"http://localhost:{port}"
        logger.info("  Waiting for server (up to %ds)...", _VLLM_STARTUP_TIMEOUT)
        if not self._wait_for_server(url):
            self._vllm_proc.terminate()
            raise RuntimeError(
                f"PromptInject vLLM server failed to start. "
                f"Check log: {log_file.name}"
            )
        logger.info("  Server ready at %s", url)
        return url

    def _stop_server(self):
        if self._vllm_proc is not None and self._vllm_proc.poll() is None:
            self._vllm_proc.terminate()
            try:
                self._vllm_proc.wait(timeout=10)
            except subprocess.TimeoutExpired:
                self._vllm_proc.kill()
            logger.info("Stopped PromptInject vLLM server (pid %d)",
                        self._vllm_proc.pid)
            self._vllm_proc = None

    # -- LLM injection text generation -----------------------------------------

    def _get_llm_client(self):
        """Lazy-init an OpenAI-compatible client, auto-starting vLLM if needed."""
        if self._llm_client is not None:
            return self._llm_client

        url = self.config.llm_server_url
        if not url:
            url = self._start_server()
            self.config.llm_server_url = url

        if not self.config.llm_model:
            self.config.llm_model = _DEFAULT_LLM_MODEL

        from openai import OpenAI
        self._llm_client = OpenAI(base_url=f"{url}/v1", api_key="dummy")
        return self._llm_client

    def _llm_generate_injection(self, target_text: str,
                                question: str = "") -> str:
        """Ask the LLM server to generate injection text."""
        client = self._get_llm_client()
        level = self.config.level
        if level == "authority":
            prompt_tpl = _INJECTION_PROMPT_AUTHORITY
        else:
            prompt_tpl = _INJECTION_PROMPT_INSTRUCTION
        prompt = (prompt_tpl
                  .replace("{target}", target_text)
                  .replace("{question}", question[:200] if question else ""))
        resp = client.chat.completions.create(
            model=self.config.llm_model,
            messages=[
                {"role": "system",
                 "content": "You are a concise assistant. /no_think"},
                {"role": "user", "content": prompt},
            ],
            max_tokens=256, temperature=0.0,
        )
        text = resp.choices[0].message.content or ""
        if "<think>" in text:
            text = re.sub(r"<think>.*?</think>\s*", "", text,
                          flags=re.DOTALL)
        if "<think>" in text:
            text = re.sub(r"<think>.*", "", text, flags=re.DOTALL)
        text = text.strip().strip("\"'.")
        if not text:
            raise RuntimeError(
                f"LLM returned empty injection text for target '{target_text}'"
            )
        return text

    # -- Target resolution -----------------------------------------------------

    def _resolve_target(self, sample: Sample) -> str:
        """Get target_text from config or sample metadata."""
        if self.config.target_text:
            return self.config.target_text
        target = sample.metadata.get("attack_target_text")
        if target:
            return target
        raise ValueError(
            f"No target_text: set --target_text or provide "
            f"metadata['attack_target_text'] on sample '{sample.id}'."
        )

    # -- Main entry point ------------------------------------------------------

    def generate(self, model, sample: Sample, **kwargs) -> AttackResult:
        target_text = self._resolve_target(sample)

        # Use pre-computed injection from metadata if the caller already ran
        # the LLM (e.g. generate.py batched it), otherwise generate here.
        injection = sample.metadata.get("attack_injection_text")
        if not injection:
            injection = self._llm_generate_injection(
                target_text, question=sample.question,
            )

        adversarial_question = sample.question.rstrip() + " " + injection

        original_output = ""
        adversarial_output = ""
        success = False

        if model is not None:
            original_output = self._run_inference_multi(
                model, sample, sample.question
            )
            adv_sample = dataclasses.replace(sample, question=adversarial_question)
            adversarial_output = self._run_inference_multi(
                model, adv_sample, adversarial_question
            )
            success = target_text.lower() in adversarial_output.lower()

        return AttackResult(
            success=success,
            adversarial_sample=sample.images[0],
            original_output=original_output,
            adversarial_output=adversarial_output,
            perturbation_norm=0.0,
            queries=2 if model is not None else 0,
            metadata={
                "target_phrase": target_text,
                "level": self.config.level,
                "injection_text": injection,
                "adversarial_question": adversarial_question,
                "attack": "promptinject",
            },
        )
